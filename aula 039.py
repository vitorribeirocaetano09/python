# operador de membro :

lista = ["vitor","ana","nicolas",12,34]

print(34 in lista)
print("vitor" in lista)
print("ana"in lista)

# exemplo 2 :

lista_1 = ["joao",22,123,6]
lista_2 = ["carlos",234,55]

# casos de false:

print("carlos" in lista_1)
print(123 in lista_2)

# caso de true :

print("joao"in lista_1)
print(234 in lista_2)

# casos de NOT :

a = [123,44,55]
print(123 not in a)

print(44 not in a)









